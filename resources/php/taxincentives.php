        <h2>Utah Solar Customers receive a state and federal tax incentive:</h2>
        <h3>State Level Incentive</h3>
        <p>The Investment Tax Credit is worth 25% of eligible system cost or $2,000, whichever is less, for residential installations, and 10% of eligible system cost or $50,000, whichever is less, for commercial installations. Eligible technologies include solar photovoltaic, solar thermal, wind, geothermal, hydro, and biomass.</p>
        <p>Please allow 3-4 weeks to receive your TC-40E.
        </p>
        <br>
        <span><a href="http://energy.utah.gov/funding-incentives/financing-for-infrastructure/">READ MORE...</a></span>
        <br>
        <h3>Federal Level Incentive</h3>
        
        <span>30% for systems placed in service by 12/31/2019</span><br>
        <span>26% for systems in service after 12/31/2019 and before 01/01/2021</span><br>
        <span>22% for systems placed in service after 12/31/2020 and before 01/01/2022</span><br>
        <span>There is no maximum credit for systems placed in service after 2008.</span><br>
        <span>The home served by the system does not have to be the taxpayer's principal residence.</span><br><br>
        
